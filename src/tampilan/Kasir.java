    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        // TODO add your handling code here:
        String jk = "";

    if (rlaki.isSelected()) {
        jk = "Laki-Laki";
    } else if (rperempuan.isSelected()) {
        jk = "Perempuan";
    }

    String sql = "INSERT INTO kasir VALUES (?,?,?,?,?,?,?)";

    try {

        PreparedStatement stat = conn.prepareStatement(sql);

        stat.setString(1, txtidkasir.getText());
        stat.setString(2, txtnama.getText());
        stat.setString(3, jk);
        stat.setString(4, txttelp.getText());
        stat.setString(5, cbagama.getSelectedItem().toString());
        stat.setString(6, txtalamat.getText());
        stat.setString(7, txtpassword.getText());

        stat.executeUpdate();

        JOptionPane.showMessageDialog(null,
                "Data berhasil disimpan");

        kosong();
        aktif();
        datatable();

    } catch (Exception e) {

        JOptionPane.showMessageDialog(null,
                "Data gagal disimpan : " + e);

    }

        
    }//GEN-LAST:event_bsimpanActionPerformed

