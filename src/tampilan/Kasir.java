   protected void datatable(){

    Object[] Baris = {
        "ID Kasir",
        "Nama",
        "Jenis Kelamin",
        "Telepon",
        "Agama",
        "Alamat",
        "Password"
    };

    tabmode = new DefaultTableModel(null, Baris);

    tblkasir.setModel(tabmode);

    String sql = "SELECT * FROM kasir";

    try{

        Statement stat = conn.createStatement();
        ResultSet hasil = stat.executeQuery(sql);

        while(hasil.next()){

            String id = hasil.getString("id_kasir");
            String nama = hasil.getString("nama");
            String jk = hasil.getString("jk");
            String telp = hasil.getString("telp");
            String agama = hasil.getString("agama");
            String alamat = hasil.getString("alamat");
            String password = hasil.getString("password");

            String[] data = {
                id,nama,jk,telp,agama,alamat,password
            };

            tabmode.addRow(data);
        }

    } catch (Exception e){
        JOptionPane.showMessageDialog(null, e);
    }
}

    

